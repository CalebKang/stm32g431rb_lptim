void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  if(GPIO_Pin == GPIO_PIN_13)
  {
    static int seq=0;

    switch(seq)
    {
      case 0:
        printf("Start value: %d\r\n", (int)LL_LPTIM_GetCounter(LPTIM1));
        LL_LPTIM_Enable(LPTIM1);
        LL_LPTIM_SetAutoReload(LPTIM1, 0xFFFF);
        LL_LPTIM_StartCounter(LPTIM1, LL_LPTIM_OPERATING_MODE_CONTINUOUS);
        seq++;
        break;

      case 1:
        printf("Reset value: %d\r\n", (int)LL_LPTIM_GetCounter(LPTIM1));
        LL_LPTIM_SetAutoReload(LPTIM1, 0xFFFF);
        LL_LPTIM_StartCounter(LPTIM1, LL_LPTIM_OPERATING_MODE_CONTINUOUS);
        seq++;
        break;

      case 2:
        printf("Counter value: %d\r\n", (int)LL_LPTIM_GetCounter(LPTIM1));

        seq++;
        break;

      case 3: //to initiate LPTIM counter
        printf("Counter value: %d\r\n", (int)LL_LPTIM_GetCounter(LPTIM1));
        LL_LPTIM_SetAutoReload(LPTIM1, 0x1);
        LL_LPTIM_StartCounter(LPTIM1, LL_LPTIM_OPERATING_MODE_ONESHOT);
        //LL_LPTIM_Disable(LPTIM1);
        seq=1;
        break;
    }
  }
}
